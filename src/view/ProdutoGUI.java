    private void adicionarTable() {
        DefaultTableModel model = (DefaultTableModel) table.getModel();

        model.setNumRows(0);

        for (Produto u : listProduto) {
            model.addRow(new Object[]{
                u.getId(),
                u.getDescricao(),
                u.getPrecoCusto(),
                u.getPrecoVenda(),
                u.getSku(),
                u.getCategoria(),
                u.getMarca(),
                u.getModelo(),
                u.getUnidadeMedida()
            });
        }

        table.setModel(model);

    }

